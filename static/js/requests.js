document.addEventListener("DOMContentLoaded", () => {
    const slider = document.querySelector(".slider");
    const sliderContainer = document.querySelector(".slider-container");
    const pagination = document.querySelector(".pagination");
  
    const certificates = data.certificates;

    // Create image elements
    certificates.forEach((src, index) => {
        const img = document.createElement("img");
        img.src = src;
        img.alt = `Slide ${index + 1}`;
        if (index === 0) img.classList.add("active");
        slider.appendChild(img);

        // Create a dot for each image
        const dot = document.createElement("div");
        dot.classList.add("dot");
        if (index === 0) dot.classList.add("active"); // First dot active
        dot.dataset.index = index; // Store index for click handling
        pagination.appendChild(dot);

        // Set initial container height when first image loads
        if (index === 0) {
        img.onload = () => {
            sliderContainer.style.height = `${img.offsetHeight}px`;
        };
        }
    });

    const images = slider.querySelectorAll("img");
    const dots = pagination.querySelectorAll(".dot");
    let currentIndex = 0;
    let intervalId;

    // Function to update the slider and dots
    function updateSlide(index) {
        images[currentIndex].classList.remove("active");
        dots[currentIndex].classList.remove("active");
        currentIndex = index;
        images[currentIndex].classList.add("active");
        dots[currentIndex].classList.add("active");
        sliderContainer.style.height = `${images[currentIndex].offsetHeight}px`;
    }

    // Automatic slideshow
    function startSlideshow() {
        intervalId = setInterval(() => {
        const nextIndex = (currentIndex + 1) % images.length;
        updateSlide(nextIndex);
        }, 5000);
    }

    // Stop slideshow temporarily on manual interaction
    function resetSlideshow() {
        clearInterval(intervalId);
        startSlideshow();
    }

    // Click event for dots
    dots.forEach(dot => {
        dot.addEventListener("click", () => {
        const index = parseInt(dot.dataset.index);
        updateSlide(index);
        resetSlideshow(); // Restart the timer after manual click
        });
    });

    // Start the slideshow
    startSlideshow();
});

function setPortfolioFilters() {
    var $container = $('.portfolioContainer');
    $container.isotope({
        filter: '*',
        animationOptions: {
            duration: 750,
            easing: 'linear',
            queue: false
        }
    });

    $('.portfolioFilter a').click(function () {
        $('.portfolioFilter .active').removeClass('active');
        $(this).addClass('active');

        var selector = $(this).attr('data-filter');
        $container.isotope({
            filter: selector,
            animationOptions: {
                duration: 750,
                easing: 'linear',
                queue: false
            }
        });
        return false;
    });
};

